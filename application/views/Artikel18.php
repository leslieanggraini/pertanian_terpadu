	<div class="content">
		<p><br>&nbsp Setiap tahunnya, kebutuhan akan jagung rendah aflatoksin (di bawah 20 ppb) diperkirakan mencapai 15 ribu ton. Jagung tersebut digunakan sebagai bahan pakan sapi perah agar menghasilkan susu segar dengan persyaratan aflatoksin maksimal 0,5 ppb. </p>

		<p><br>&nbsp Kebutuhan tersebut sangat kecil dibandingkan dengan produksi jagung nasional. Produksi jagung rendah aflatoksin memerlukan penanganan khusus mulai dari budidaya, penanganan pascapanen sampai distribusi kepada peternak sapi perah yang membutuhkan. Upaya petani menghasilkan jagung rendah aflatoksin harus dibarengi insentif harga yang memadai dari pembeli (peternak sapi perah).</p>
		
		<p><br> &nbsp Direktur PPHTP Ditjen Tanaman Pangan Kementan, Gatut Sumbogodjati mengatakan,  pengujian pada sampel jagung di lahan petani pada 2—7 hari menjelang panen di beberapa daerah sentra jagung menunjukkan kandungan aflatoksin masih di bawah 20 ppb (tertinggi 1,8 ppb).</p>

		<p><br> &nbsp “Jagung dengan aflatoksin rendah sangat dimungkinkan disediakan di dalam negeri dengan ketentuan dipanen pada umur maksimal (ditandai dengan kelobot dan daun yang berwarna cokelat), proses panen  menggunakan corn combine harvester dan dalam waktu maksimal empat jam harus langsung dilakukan pengeringan menggunakan dryer untuk mempertahankan kontaminasi aflatoksin agar tetap dibawah 20 ppb,” kata Gatut dalam keterangan tertulis</p>

		<p><br> &nbsp Kandungan aflatoksin di bawah 20 ppb pada jagung dapat dipertahankan dengan panen menggunakan corn combine harvester (mempercepat proses panen) dan proses pengeringan menggunakan dryer.</p>

		<p><br> &nbsp Mengingat kepemilikan lahan petani jagung relatif kecil, untuk menghasilkan jagung rendah aflatoksin dalam jumlah yang memadai diperlukan perjanjian khusus Business to Business antara petani dan peternak sapi perah atau industri susu segar. Poin penting dalam perjanjian tersebut, menurut Gatut, adalah luas lahan jagung, kualitas, volume, harga, dan pola pasokan.</p>

		<p><br> &nbsp Gatut menambahkan, pemerintah siap memfasilitasi kebutuhan alsintan yang diperlukan (Corn Combine Harvester), pengering (dryer) dan mendampingi pengembangan Jagung rendah aflatoksin di area yang disepakati.</p>

		<p><br> &nbsp Jagung rendah aflatoksin tidak hanya tanggung jawab pemerintah, tetapi peternak sapi perah atau industri susu segar juga memiliki tanggung jawab mengawal budidaya sampai penanganan pascapanen dan proses distribusi jagung rendah aflatoksin pascaperjanjian <i> Business to Business</i>.</p>
		
		<p><br> &nbsp Berdasarkan informasi yang diperoleh dari P. Green Fields sebagai salah satu industri susu segar, karakteristik mutu yang dipersyaratkan disamping kandungan aflatoksin adalah kadar air maksimal 15%. Secara tematik, Indonesia memiliki segala sumber daya untuk menghasilkan jagung rendah aflatoksin.</p>

		<p><br> &nbsp Lebih lanjut Gatut mengatakan, pemerintah telah memfasilitasi biaya pengujian mutu jagung di daerah sentra sentra jagung untuk membantu kelompok tani mengetahui tingkat mutu jagung yang dihasilkan. Gatut menjelaskan, Indonesia beruntung memiliki dua musim tanam jagung.</p>

		<p><br> &nbsp Penetapan daerah tertentu sebagai kawasan jagung menjadi titik awal pengembangan produksi  jagung rendah aflatoksin ke depan. Pengembangan produksi jagung rendah aflatoksin harus dilakukan dalam bentuk kawasan karena kepemilikan lahan petani kecil-kecil.</p>

		<p><br> &nbsp “Pengawalan produksi jagung rendah aflatoksin mulai dari proses budidaya sampai dengan penanganan pascapanen harus dilakukan tidak hanya oleh produsen jagung, tetapi juga oleh peternak sapi perah sebagai mitra,” pungkas Gatut.</p>

